package ControlStatementLoop;

public class DoWhileLoopme {
	public static void main(String[] args) {
		int i = 12;
		do {
			System.out.println(i);
			i++;
		}while(i>=20);  // suppose the condition is false the 12 only executed 
		                // the the do is already printed i value after the increment the condition is fail so the 
		               // already completed value is executed  // exit level chek
	}

}